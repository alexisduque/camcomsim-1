package fr.rtone.vlc.simulator.network;

import java.util.ArrayList;
import java.util.List;
import java.util.Map;
import java.util.concurrent.ConcurrentHashMap;
import java.util.function.Function;
import java.util.function.Predicate;
import java.util.stream.Collectors;

public class Message<T> extends ArrayList<T> {

    public static <T> Predicate<T> distinctByKey(Function<? super T, Object> keyExtractor) {
        Map<Object, Boolean> map = new ConcurrentHashMap<>();
        return t -> map.putIfAbsent(keyExtractor.apply(t), Boolean.TRUE) == null;
    }

    public Message<T> getUnique() {
        List<T> distinctElements = this.stream().distinct().filter(distinctByKey(p -> p.hashCode())).collect
                (Collectors.toList());
        Message<T> unique = new Message<>();
        unique.addAll(distinctElements);
        return unique;
    }

    public int countUnique() {
        return getUnique().size();
    }

    public boolean isCompleted(int expected) {
        return (countUnique() >= expected);
    }
}
